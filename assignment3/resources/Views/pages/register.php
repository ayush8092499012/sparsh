<script>
  document.getElementById("registerBtn").addEventListener("click", function () {
    validateForm();
  });

  function validateForm() {
    // Clear previous error messages
    document.getElementById("firstnameError").textContent = "";
    document.getElementById("lastnameError").textContent = "";
    document.getElementById("emailError").textContent = "";
    document.getElementById("passwordError").textContent = "";
    document.getElementById("cpasswordError").textContent = "";
    document.getElementById("termsError").textContent = "";

    // Hide the alert initially
    const alertBox = document.querySelector(".alert-danger");
    alertBox.style.display = "none";

    let isValid = true;

    // Validate First Name
    isValid =
      validateField(
        "firstname",
        /^[A-Za-z]{2,}$/,
        "First name is required and must be at least 2 characters long, containing only alphabets.",
        "firstnameError"
      ) && isValid;

    // Validate Last Name
    isValid =
      validateField(
        "lastname",
        /^[A-Za-z]{2,}$/,
        "Last name is required and must be at least 2 characters long, containing only alphabets.",
        "lastnameError"
      ) && isValid;

    // Validate Email (must contain @gmail.com)
    isValid =
      validateField(
        "email",
        /^[^\s@]+@gmail\.com$/,
        "Please enter a valid Gmail address (e.g., yourname@gmail.com).",
        "emailError"
      ) && isValid;

    // Validate Password (Minimum 8 characters)
    isValid =
      validateField(
        "password",
        /^.{8,}$/,
        "Password is required and must be at least 8 characters long.",
        "passwordError"
      ) && isValid;

    // Validate Confirm Password (must match Password)
    const password = document.getElementById("password").value;
    isValid =
      validateField(
        "cpassword",
        new RegExp(`^${password}$`),
        "Confirm password is required and must match the password.",
        "cpasswordError"
      ) && isValid;

    // Validate Terms and Conditions
    const terms = document.getElementById("terms-conditions");
    if (!terms.checked) {
      document.getElementById("termsError").textContent =
        "You must agree to the terms and conditions.";
      terms.style.outline = "1px solid red";
      isValid = false;
    } else {
      document.getElementById("termsError").textContent = "";
      terms.style.outline = "";
    }

    // Show alert if there are errors, hide it if the form is valid
    if (!isValid) {
      alertBox.style.display = "block";
    } else {
      alertBox.style.display = "none";

      // Collect form data
      const formData = {
        firstname: $('#firstname').val(),
        lastname: $('#lastname').val(),
        email: $('#email').val(),
        password: $('#password').val(),
        cpassword: $('#cpassword').val(),
        terms: $('#terms-conditions').is(':checked')
      };


      $("#registerBtn")
        .prop("disabled", true)
        .html(`Creating account...${spinner}`);

      $.ajax({
        url: '/api/create-user',
        type: 'POST',
        data: formData,
        dataType: "json",
        success: function (response) {
          $("#registerBtn")
            .prop("disabled", false)
            .html('Sign Up');

          $('#formAuthentication')[0].reset();

          if (response.status == 'success') {
            Swal.fire({
              title: "Registration successfully!",
              text: response.message,
              icon: "success",
              timer: 2000, // Close after 2 seconds
              showConfirmButton: false,
            }).then(() => {             
              window.location.href = '/dashboard'; 
            });;
          } else if (response.status == 'warning') {
            Swal.fire({
              title: "Email id already taken!",
              text: response.message,
              icon: "warning",
              timer: 2000, // Close after 2 seconds
              showConfirmButton: false,
            });
          } else {
            Swal.fire({
              title: "Opps!",
              text: response.message,
              icon: "error",
              timer: 2000, // Close after 2 seconds
              showConfirmButton: false,
            });
          }
        },
        error: function (jqXHR, textStatus, errorThrown) {
          console.error('Error:', textStatus, errorThrown);
          $("#registerBtn")
            .prop("disabled", false)
            .html('Sign Up');

          Swal.fire({
            title: "Error!",
            text: "There was an error during registration. Please try again.",
            icon: "error",
            timer: 2000, // Close after 2 seconds
            showConfirmButton: false,
          });
        }
      });
    }
  }

  // Helper function to validate individual fields
  function validateField(fieldId, regex, errorMessage, errorId) {
    const field = document.getElementById(fieldId);
    const fieldValue = field.value.trim();
    const errorElement = document.getElementById(errorId);

    if (fieldValue === "") {
      errorElement.textContent = `${fieldId.replace(/^\w/, (c) =>
        c.toUpperCase()
      )} is required.`;
      field.style.borderColor = "red";
      return false;
    } else if (!regex.test(fieldValue)) {
      errorElement.textContent = errorMessage;
      field.style.borderColor = "red";
      return false;
    } else {
      errorElement.textContent = "";
      field.style.borderColor = "";
      return true;
    }
  }

  // Event listeners for real-time validation on input
  document.querySelectorAll("#formAuthentication input").forEach((input) => {
    input.addEventListener("input", () => {
      const regexes = {
        firstname: /^[A-Za-z]{2,}$/,
        lastname: /^[A-Za-z]{2,}$/,
        email: /^[^\s@]+@gmail\.com$/,
        password: /^.{8,}$/,
        cpassword: new RegExp(`^${document.getElementById("password").value}$`),
      };

      const errorIds = {
        firstname: "firstnameError",
        lastname: "lastnameError",
        email: "emailError",
        password: "passwordError",
        cpassword: "cpasswordError",
      };

      const errorMessages = {
        firstname:
          "First name must be at least 2 characters long and contain only alphabets.",
        lastname:
          "Last name must be at least 2 characters long and contain only alphabets.",
        email: "Please enter a valid Gmail address (e.g., yourname@gmail.com).",
        password: "Password must be at least 8 characters long.",
        cpassword: "Confirm password must match the password.",
      };

      if (input.id in regexes) {
        validateField(
          input.id,
          regexes[input.id],
          errorMessages[input.id],
          errorIds[input.id]
        );
      }
    });
  });

  // Event listener for the terms and conditions checkbox to remove error on change
  document
    .getElementById("terms-conditions")
    .addEventListener("change", function () {
      const termsError = document.getElementById("termsError");
      termsError.textContent = "";
      this.style.outline = "";
    });

  // Event listener to handle Enter key press
  document
    .getElementById("formAuthentication")
    .addEventListener("keydown", function (event) {
      if (event.key === "Enter") {
        event.preventDefault(); // Prevent default form submission
        document.getElementById("registerBtn").click(); // Trigger the click event on the button
      }
    });

</script>